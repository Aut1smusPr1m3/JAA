def test_process_gcode_with_header_succeeds():
    gcode_with_header = [
        "; Generated by BambuStudio\n",
        "; Sliced by OrcaSlicer\n",
        "G28\n",
        "G92 X0 Y0 Z0 E0\n",
        "; EXECUTABLE_BLOCK_START\n",
        "G1 X10 Y0 Z0.2 F3000\n",
        "G1 X20 Y0 Z0.2 F3000\n",
        "; EXECUTABLE_BLOCK_END\n",
    ]

    result = process_gcode(gcode_with_header, ".")
    assert len(result) == len(gcode_with_header)


